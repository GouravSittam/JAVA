public class bit {
    public static void main(String[] args) {
        int a=9,b=10;
        System.out.println("Given numbers output are: "+(a&b));
        System.out.println("Given numbers output are: "+(a|b));
        System.out.println("Given numbers output are: "+(a^b));
        System.out.println("Given numbers output are: "+(a<<1));
        System.out.println("Given numbers output are: "+(a<<2));
        System.out.println("Given numbers output are: "+(b<<1));
        System.out.println("Given numbers output are: "+(b<<2));
        System.out.println("Given numbers output are: "+(a>>1));
        System.out.println("Given numbers output are: "+(a>>2));
        System.out.println("Given numbers output are: "+(b>>1));
        System.out.println("Given numbers output are: "+(b>>2));
    }
    
}
